class nestedforloop{
    static public void main(String args[]){
        int i;
        int j;
    int rows = 12;

for (i= rows; i<= 12; i--) { 
  
            // loop to print the number of stars in each row 
            for (j = 1; j <= 12 ; j++) { 
                System.out.print("* "); 
            } 
  
            // for new line after printing each row 
            System.out.println(); 
        } 
    } }
